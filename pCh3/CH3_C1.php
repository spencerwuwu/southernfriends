<div class="small-topic-w">
<h4>法令</h4>
</div>
</div>
<p>依據就業服務法第57條第9款規定，雇主聘僱外國人不得有其他違反就業服務法或依就業服務法所發布之命令。依據雇主聘僱外國人許可及管理辦法第27條之1規定，雇主申請聘僱外國人者，應於外國人入國3日內，檢附應備文件通知當地主管機關實施檢查。依據雇主聘僱外國人許可及管理辦法第28條規定，雇主應於外國人入國後15日內申請聘僱許可。</p>
