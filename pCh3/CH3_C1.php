<div class="small-topic-notice">
<img src="img/Ch3/icon_notice.png" />
</div>
<div class="small-topic-w">
<h4>注意事項</h4>
</div>
</div>
<p>雇主經許可引進或接續聘僱第二類外國人時，應於外國人入國或接續聘僱後3日內，檢附外國人入國通報單、外國人名冊兩份、外國人生活照顧服務計畫書、外國人入國工作費用及工資切結書通知當地主管機關。雇主經許可引進或接續聘僱第二類外國人時，應於外國人入國或接續聘僱後15日內，檢附申請書、入國3日內或入國日前3個月內至指定醫院接受健康檢查之證明正本、審查收據正本、外國人入國通報證明書等相關文件，向勞動部勞動力發展署申請聘僱許可。若外籍勞工已更換護照者，需檢附新、舊護照首頁影印本，護照延期者，請檢附護照首頁及延期首頁影印本，一併辦理資料異動事宜，並特別留意外籍勞工外僑居留證的居留期限；居留證期限需與外籍勞工聘僱期滿日相符，如未相同請先展延護照期限後再展延居留證期限。</p>
