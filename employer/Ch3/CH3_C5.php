<h4>法令</h4>
</div>
</div>
<ol>
<li>依據就業服務法第57條第5款規定，雇主聘雇外國人不得有未依歸定安排所聘雇之外國人接受健康檢查，或未依規定將健康檢查結果含報衛生主管機關之情事。</li>
<li>依據受聘雇外國人健康檢查管理辦法第5條規定，雇主應於從事就業服務法第46條第1項第8款至第11款規定之外國人入國工作滿6個月、18個月及30個月之日前後30日內，安排其至指定醫院接受定期健康檢查。又同辦法第7條及第8條規定，若外勞有健康檢查不合格項目或無法確診，安排上述人員進行再檢查，雇主應於收受指定醫院核發健康檢查複檢證明之日15日內，檢具文件送交所在地衛生主管機關查備。</li>
<li>依據就業服務法第40條第1項第15款規定，私立就業服機構及其從業人員從事就業服務業務，不得未善進受任事物，致雇主違反本法或依本法所發佈之命令。</li>
</ol>
