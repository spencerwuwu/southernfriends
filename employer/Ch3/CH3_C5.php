<div class="one item content" markdown="1"><h3 id="c5">C5</h3>
<p><img src="img/Ch3/C5.png" alt="" />
順天工廠聘雇國際移工宋財，因工廠趕工，宋財無法配合仲介所安排的時間去做健康檢查，簽訂切結書並自行依歸定在來臺工作滿18個月前後30日內辦理健康檢查。然之後檢查之一呈現陽性反應，經複檢合格後，因為工廠負責人過忙忘了將健康檢查證明送至當地政府衛生局備查</p>
